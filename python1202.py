#2. Assignment Operator
a = 10

#2.1.Assign value
b = a
print("Assign value : ",b)

#2.2. Add and assign vale
b += a #b=>: b + a
print("Assign value : ",b) #=>:10

#2.3. Subtract and assign vale
b += a #b=>: b - a
print("Assign value : ",b) #=>:20

#2.4. Multiply and assign vale
b += a #b=>: b * a
print("Assign value : ",b) #=>:100

#2.5. Bitwise lishift operator
b += a #b=>: b << a
print("Assign value : ",b) #=>:?