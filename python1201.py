#1.Arithemetic Operator
#==============

a = 100
b = 50
#1.Addition:หาผลบวก
print("เรามีเงิน:",a + b,"บาท")

#2.subtraction:หาผลลบ
print("เรามีเงิน:",a - b,"บาท")

#3.Multiplication:หาผลคูณ
print("เรามีเงิน:",a * b,"บาท")

#4.Division:หาผลหาร
print("เรามีเงิน:",a / b,"บาท")

#5.Moduius:หาผลหารเศษที่เหลือ
print("เรามีเงิน:",a % b,"บาท")

#6.powe:หาผลและยกกำลัง
print("เรามีเงิน:",a ** b,"บาท")

